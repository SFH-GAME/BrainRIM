            <h2 class="page-title2">4 полезных совета</h2>
            <div class="steps">
                <div class="step-number">1.</div>
                <div class="step-title">Слушай своё тело</div>
                <div class="step-description">Иногда наш организм сам подсказывает, чего ему не хватает. Чувствуешь усталость? Возможно, тебе нужно больше продуктов с железом, таких как зелёные овощи или мясо. Тянет на сладкое? Попробуй заменить конфеты на фрукты, чтобы получить энергию без лишнего сахара.</div>

                <div class="step-number">2.</div>
                <div class="step-title">Составляй тарелку гармонично</div>
                <div class="step-description">Старайся, чтобы половину твоей тарелки занимали овощи и фрукты, четверть — белки (мясо, рыба, бобовые), а оставшуюся часть — полезные углеводы (цельнозерновые продукты). Такой подход поможет получить максимум питательных веществ и оставаться сытым.</div>

				<div class="step-number">3.</div>
                <div class="step-title">Экспериментируй с вкусами</div>
                <div class="step-description">Еда не должна быть скучной! Пробуй новые специи, сочетай разные кухни мира и экспериментируй с рецептами. Это поможет тебе наслаждаться процессом приготовления и открывать для себя полезные альтернативы привычным блюдам.</div>

				<div class="step-number">4.</div>
                <div class="step-title">Не забывай про фастфуд</div>
                <div class="step-description">Фастфуд часто воспринимается как "враг" здорового питания, но важно помнить, что баланс — это ключ.<br>Если уж выбираешь фастфуд, делай осознанный выбор: выбирай блюда с меньшим содержанием жира, сахара или добавок, иногда можно готовить "фастфуд" дома: например, домашний бургер с цельнозерновой булочкой, нежирным мясом и свежими овощами, позволь себе насладиться фастфудом, но избегай превращения его в привычку.</div>

